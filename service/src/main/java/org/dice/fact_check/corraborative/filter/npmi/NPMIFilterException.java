package org.dice.fact_check.corraborative.filter.npmi;

public class NPMIFilterException extends Exception {

    private static final long serialVersionUID = 1L;

    public NPMIFilterException() {
        super();
    }

    public NPMIFilterException(String message) {
        super(message);
    }

    public NPMIFilterException(Throwable cause) {
        super(cause);
    }

    public NPMIFilterException(String message, Throwable cause) {
        super(message, cause);
    }

}
